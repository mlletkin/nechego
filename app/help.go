var noCategory = UserError("–¢–∞–∫–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –Ω–µ—Ç.")

func (a *App) handleHelp(c tele.Context) error {
	category := getMessage(c).Argument()
	if category != "" {
		out, ok := categories[category]
		if !ok {
			return respondUserError(c, noCategory)
		}
		return respond(c, categoryResponse(category, out))
	}
	return respond(c, helpResponse())
}

func categoryResponse(category, help string) Response {
	return Response(joinLines(helpHeader(category), help))
}

func helpResponse() Response {
	return Response(joinLines(helpHeader("–±–∞–∑–æ–≤—ã–µ –∫–æ–º–∞–Ω–¥—ã"), string(formatHelp())))
}

func formatHelp() HTML {
	return HTML(fmt.Sprintf(help, availableCategories()))
}

func availableCategories() string {
	a := []string{}
	for c := range categories {
		a = append(a, fmt.Sprintf("<code>%s</code>", c))
	}
	return strings.Join(a, ", ")
}

func helpHeader(category string) string {
	return fmt.Sprintf("üóÇ –ü–æ–º–æ—â—å: <b>%s</b>.", category)
}
